class Main {
  public static void main(String[] args) {
   //backspace
    System.out.println("Hello world!\b");
   //Form Feed
    System.out.println("Hello world!\f");
   //Double Quote
    System.out.println("Hello world!\"");
    //Carriage Return
    System.out.println("Hello world!\r");
    //Tab
    System.out.println("Hello world!\t");
  }
}
